#include "../../minishell.h"

void	echo(char **av)
{
	int	i;
	int	n;

	i = 1;
	n = 1;
	while (av[i] && !ft_strcmp(av[i], "-n"))
	{
		n = 0;
		i++;
	}
	while (av[i])
	{
		printf("%s", av[i]);
		if (av[++i])
			printf(" ");
	}
	if (n == 1)
		printf("\n");
}
